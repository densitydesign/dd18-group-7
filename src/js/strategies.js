// Regulatory statements
let regulatoryStatementsContentCopyW1 = document.getElementById(
  "regulatory-statements-content-copy-w1"
);
regulatoryStatementsContentCopyW1.addEventListener("mouseover", (event) => {
  regulatoryStatementsContentCopyW1.classList.remove("opacity-0");
  regulatoryStatementsContentCopyW1.classList.add("opacity-100");
});
regulatoryStatementsContentCopyW1.addEventListener("mouseleave", (event) => {
  regulatoryStatementsContentCopyW1.classList.remove("opacity-100");
  regulatoryStatementsContentCopyW1.classList.add("opacity-0");
});

